DROP TABLE IF EXISTS users CASCADE;
DROP TABLE IF EXISTS categories CASCADE;
DROP TABLE IF EXISTS events CASCADE;
DROP TABLE IF EXISTS requests CASCADE;
DROP TABLE IF EXISTS compilations CASCADE;
DROP TABLE IF EXISTS events_compilations CASCADE;
CREATE TABLE IF NOT EXISTS users
    (
        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        name VARCHAR(255) NOT NULL,
        email VARCHAR(512) NOT NULL,
        CONSTRAINT UQ_USER_EMAIL UNIQUE (email)
        );

CREATE TABLE IF NOT EXISTS categories
(
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255)  NOT NULL,
    CONSTRAINT UQ_CATEGORY_NAME UNIQUE (name)
    );


CREATE TABLE IF NOT EXISTS events
(
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    annotation VARCHAR(3000) NOT NULL,
    title VARCHAR(100) NOT NULL,
    description VARCHAR(3000) NOT NULL,
    initiator_id BIGINT NOT NULL,
    request_moderation BOOLEAN Not NULL,
    CONSTRAINT FK_EVENT_INITIATOR FOREIGN KEY (initiator_id) REFERENCES users (id),
    category_id BIGINT NOT NULL,
    CONSTRAINT FK_EVENT_CATEGORY FOREIGN KEY (category_id) REFERENCES categories (id)  ON UPDATE CASCADE,
    paid BOOLEAN NOT NULL,
    publishet_on TIMESTAMP WITHOUT TIME ZONE,
    event_date TIMESTAMP WITHOUT TIME ZONE NOT NULL,
    created TIMESTAMP WITHOUT TIME ZONE NOT NULL,
    participant_limit INT NOT NULL,
    location_lat  NUMERIC NOT NULL,
    location_lon  NUMERIC NOT NULL,
    state VARCHAR(30)
    );

CREATE TABLE IF NOT EXISTS requests
(
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    event_id BIGINT NOT NULL,
    CONSTRAINT FK_REQUEST_EVENT FOREIGN KEY (event_id) REFERENCES events (id),
    requester_id BIGINT NOT NULL,
    CONSTRAINT FK_REQUEST_REQUESTER FOREIGN KEY (requester_id) REFERENCES users (id),
    status  VARCHAR(255) NOT NULL,
    CONSTRAINT UQ_PARTICIPANT_PER_EVENT UNIQUE (requester_id, event_id),
    created TIMESTAMP WITHOUT TIME ZONE NOT NULL
    );

CREATE TABLE IF NOT EXISTS compilations
(
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    pinned BOOLEAN NOT NULL,
    title VARCHAR(100)
    );

CREATE TABLE IF NOT EXISTS events_compilations
(
    event_id BIGINT NOT NULL REFERENCES events (id) ON UPDATE CASCADE,
    compilation_id BIGINT  NOT NULL REFERENCES compilations (id) ON UPDATE CASCADE,
    CONSTRAINT event_compilation_pk PRIMARY KEY (event_id ,compilation_id)
    );